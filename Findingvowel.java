
public class Findingvowel {
    public static void main(String args[]){
        char ch = 'b';

        if (ch == 'a' || ch == 'e' || ch == 'i' || ch == 'o' || ch == 'u' || ch == 'A' || ch == 'E' || ch == 'I' || ch == 'O' || ch == 'U'){
            System.out.println("The "+ ch + " is an Vowel");
        }else{
            System.out.println("The " + ch + " is an consonants");
        }
    }
}
