public class swapvalues {
    public static void main(String[] args){
        int a = 5, b = 10;
        System.out.println("Before Swapping: A = " + a + "B = "+ b);
        int temp = a;
        a = b;
        b = temp;
        System.out.println("After Swapping: A = " + a + "n = " +b);
        }
}
